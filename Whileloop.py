# This program ships packages from one room to another

n = int(input("How many packages are there: "))
while n > 0:
    print(str(n) + " Package collected ⌻")
    print(' ')
    print(" . . . . . . . . . ")
    print(' ')
    print("Package Delivered ☻ ")
    print(' ')
    n = n - 1
    print(' ')
print("All packages moved successfully ☃ ")
